import mongoose from "mongoose";

const UserSchema = new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true
    },
   
})

const AdminModel = mongoose.models.adminModel || mongoose.model('adminModel', UserSchema)
export default AdminModel